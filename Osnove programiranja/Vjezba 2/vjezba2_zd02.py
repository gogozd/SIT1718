broj_1 = float(input('Unesi prvi broj: '))
broj_2 = float(input('Unesi drugi broj: '))

print('Suma', broj_1, 'i', broj_2, 'je', broj_1 + broj_2)
print('Razlika', broj_1, 'i', broj_2, 'je', broj_1 - broj_2)
print('Umnožak', broj_1, 'i', broj_2, 'je', broj_1 * broj_2)
print('Kvocijent', broj_1, 'i', broj_2, 'je', broj_1 / broj_2)
print('Cjelobrojni kvocijent', broj_1, 'i', broj_2, 'je', broj_1 // broj_2)
print('Potencija', broj_1, 'na', broj_2, 'je', broj_1 ** broj_2)
print('Ostatak pri cjelobrojnom dijeljenju', broj_1, 'i', broj_2, 'je', broj_1 % broj_2)
